// Categorize heroes by primary attribute from API data
export function categorizeHeroesByAttribute(heroes) {
  if (!heroes) return { Strength: [], Agility: [], Intelligence: [], Universal: [] }

  const categories = {
    Strength: [],
    Agility: [],
    Intelligence: [],
    Universal: []
  }

  heroes.forEach(hero => {
    const attrEnum = hero.stats?.primaryAttributeEnum
    const attrKey = ATTRIBUTE_MAP[attrEnum] || "Universal"

    categories[attrKey].push({
      ...hero,
      chineseName: HEROES_CN_BY_SHORTNAME[hero.shortName] || hero.displayName || hero.shortName,
      attributeKey: attrKey
    })
  })

  return categories
}

// Map API lane values to Chinese
const LANE_MAP = {
  "SAFE_LANE": "劣势路",
  "MID_LANE": "中路",
  "OFF_LANE": "优势路",
  "JUNGLE": "打野"
}

// Get lane display name based on player's radiant status
// API uses Radiant perspective: bottomLaneOutcome = Radiant's safe lane (bottom)
// For Dire players, this maps to their OFF lane (top on map)
function getLaneDisplayName(apiLane, isRadiant) {
  const laneType = LANE_MAP[apiLane]
  if (!laneType) return "其他"

  if (laneType === "中路") return "中路"
  if (laneType === "打野") return "打野"

  // For safe/off lanes, swap based on team
  if (isRadiant) {
    return laneType === "劣势路" ? "下路" : "上路"
  } else {
    // Dire: safe lane (bottom API) = their off lane (top on map) = 上路
    // Dire: off lane (top API) = their safe lane (bottom on map) = 下路
    return laneType === "劣势路" ? "上路" : "下路"
  }
}

// Get API outcome field name based on lane display name and isRadiant
function getOutcomeField(laneDisplay, isRadiant) {
  if (laneDisplay === "下路") return "bottomLaneOutcome"
  if (laneDisplay === "上路") return "topLaneOutcome"
  if (laneDisplay === "中路") return "midLaneOutcome"
  return null
}

// Analyze lane performance for a specific player
export function analyzePlayerLanePerformance(steamId, matches, heroes) {
  if (!matches || !matches.length) return null

  // Create hero lookup map
  const heroMap = {}
  heroes.forEach(h => {
    heroMap[h.id] = h
  })

  const stats = {
    totalLanes: 0,
    wins: 0,
    losses: 0,
    laneDetails: {
      "上路": { total: 0, wins: 0, losses: 0 },
      "中路": { total: 0, wins: 0, losses: 0 },
      "下路": { total: 0, wins: 0, losses: 0 },
      "打野": { total: 0, wins: 0, losses: 0 },
      "其他": { total: 0, wins: 0, losses: 0 }
    }
  }

  const matchResults = []

  matches.forEach(match => {
    // Find the player in this match
    const player = match.players?.find(p => p.steamAccountId === parseInt(steamId))
    if (!player) return

    // Get lane display name based on player's team (天辉/夜魇)
    const apiLane = player.lane
    const lane = getLaneDisplayName(apiLane, player.isRadiant)
    const outcomeField = getOutcomeField(lane, player.isRadiant)
    const outcome = outcomeField ? match[outcomeField] : null

    // Determine if player won this lane
    let laneWin = null
    let isStomp = false
    if (outcome === "RADIANT_VICTORY" || outcome === "RADIANT_STOMP") {
      laneWin = player.isRadiant
      isStomp = outcome === "RADIANT_STOMP"
    } else if (outcome === "DIRE_VICTORY" || outcome === "DIRE_STOMP") {
      laneWin = !player.isRadiant
      isStomp = outcome === "DIRE_STOMP"
    } else if (outcome === "TIE") {
      laneWin = null
    }

    // Update stats (use lane type for grouping: 上路/中路/下路)
    if (laneWin !== null && lane !== "打野" && lane !== "其他") {
      stats.totalLanes++
      if (laneWin) {
        stats.wins++
      } else {
        stats.losses++
      }
      stats.laneDetails[lane].total++
      if (laneWin) {
        stats.laneDetails[lane].wins++
      } else {
        stats.laneDetails[lane].losses++
      }
    }

    // Get hero name
    const hero = heroMap[player.heroId]
    const heroName = hero ? (hero.shortName || hero.displayName) : `Hero ${player.heroId}`

    matchResults.push({
      matchId: match.id,
      heroId: player.heroId,
      heroName: heroName,
      heroChineseName: HEROES_CN_BY_SHORTNAME[heroName] || hero?.displayName || heroName,
      lane: lane,
      laneRaw: apiLane,           // 原始API值
      position: player.position,  // 位置
      role: player.role,          // 角色
      outcome: outcome,          // 对线结果原始值
      isStomp: isStomp,
      laneWin: laneWin,           // 对线是否获胜
      win: player.isVictory,      // 比赛是否获胜
      kills: player.kills,
      deaths: player.deaths,
      assists: player.assists,
      duration: match.durationSeconds,
      startDateTime: match.startDateTime
    })
  })

  return {
    summary: {
      totalMatches: matchResults.length,
      totalLanes: stats.totalLanes,
      laneWinRate: stats.totalLanes > 0 ? ((stats.wins / stats.totalLanes) * 100).toFixed(1) + "%" : "N/A",
      wins: stats.wins,
      losses: stats.losses,
      overallWinRate: matchResults.length > 0 ? (((matchResults.filter(m => m.win).length / matchResults.length) * 100).toFixed(1)) + "%" : "N/A"
    },
    byLane: {
      "上路": stats.laneDetails["上路"].total > 0 ? {
        winRate: ((stats.laneDetails["上路"].wins / stats.laneDetails["上路"].total) * 100).toFixed(1) + "%",
        ...stats.laneDetails["上路"]
      } : null,
      "中路": stats.laneDetails["中路"].total > 0 ? {
        winRate: ((stats.laneDetails["中路"].wins / stats.laneDetails["中路"].total) * 100).toFixed(1) + "%",
        ...stats.laneDetails["中路"]
      } : null,
      "下路": stats.laneDetails["下路"].total > 0 ? {
        winRate: ((stats.laneDetails["下路"].wins / stats.laneDetails["下路"].total) * 100).toFixed(1) + "%",
        ...stats.laneDetails["下路"]
      } : null,
      "打野": stats.laneDetails["打野"].total > 0 ? {
        winRate: ((stats.laneDetails["打野"].wins / stats.laneDetails["打野"].total) * 100).toFixed(1) + "%",
        ...stats.laneDetails["打野"]
      } : null
    },
    matches: matchResults
  }
}

// Get three lanes situation for a specific match
export function getThreeLanesSituation(steamId, match, heroes) {
  const player = match.players?.find(p => p.steamAccountId === parseInt(steamId))
  if (!player) return null

  const isRadiant = player.isRadiant
  const hero = heroes.find(h => h.id === player.heroId)
  const heroName = hero ? (hero.shortName || hero.displayName) : `Hero ${player.heroId}`

  // Map outcome to win/loss for the player
  const mapOutcome = (outcome) => {
    if (!outcome) return null
    if (outcome === "RADIANT_VICTORY" || outcome === "RADIANT_STOMP") {
      return isRadiant ? "胜" : "负"
    }
    if (outcome === "DIRE_VICTORY" || outcome === "DIRE_STOMP") {
      return isRadiant ? "负" : "胜"
    }
    if (outcome === "TIE") return "平"
    return null
  }

  // Determine stomp (碾压) situation
  const mapStomp = (outcome) => {
    if (outcome === "RADIANT_STOMP" || outcome === "DIRE_STOMP") return true
    return false
  }

  // Get color based on outcome
  const getLaneColor = (outcome, isStomp) => {
    if (!outcome) return "#999"
    if (isStomp) return outcome === "胜" ? "#e91e63" : "#f44336"
    if (outcome === "胜") return "#4caf50"
    if (outcome === "负") return "#f44336"
    return "#ff9800"
  }

  // Get lane name from API field (天辉视角)
  const getLaneName = (field) => {
    if (field === "topLaneOutcome") return "上路"
    if (field === "midLaneOutcome") return "中路"
    if (field === "bottomLaneOutcome") return "下路"
    return "未知"
  }

  // Build lanes data - always show from Radiant perspective (top/mid/bottom)
  const rawLanes = {
    top: { field: "topLaneOutcome", raw: match.topLaneOutcome },
    mid: { field: "midLaneOutcome", raw: match.midLaneOutcome },
    bottom: { field: "bottomLaneOutcome", raw: match.bottomLaneOutcome }
  }

  // Convert to display format with player's team perspective
  const lanes = {}
  for (const [key, data] of Object.entries(rawLanes)) {
    const outcome = mapOutcome(data.raw)
    const isStomp = mapStomp(data.raw)

    // Get display lane name based on player's team
    let displayName
    if (isRadiant) {
      displayName = key === "top" ? "上路" : key === "mid" ? "中路" : "下路"
    } else {
      // 夜魇视角：上路<->下路互换
      displayName = key === "top" ? "下路" : key === "mid" ? "中路" : "上路"
    }

    lanes[key] = {
      name: displayName,
      outcome: outcome,
      isStomp: isStomp,
      color: getLaneColor(outcome, isStomp),
      raw: data.raw
    }
  }

  return {
    matchId: match.id,
    didRadiantWin: match.didRadiantWin,
    playerTeam: isRadiant ? "天辉" : "夜魇",
    heroId: player.heroId,
    heroName: heroName,
    heroChineseName: HEROES_CN_BY_SHORTNAME[heroName] || hero?.displayName || heroName,
    isVictory: player.isVictory,
    duration: match.durationSeconds,
    startDateTime: match.startDateTime,
    lanes: lanes
  }
}

// Calculate KDA ratio
function calcKDA(kills, deaths, assists) {
  if (deaths === 0) return (kills + assists).toFixed(1)
  return ((kills + assists) / deaths).toFixed(1)
}

// Analyze lane situation for a single match
export function analyzeLaneSituation(matchData) {
  const { laneWin, win, kills, deaths, assists, lane, outcome, isStomp, isTie, isJungle } = matchData

  // For jungle, use different logic based on KDA and match outcome
  if (isJungle) {
    return analyzeJungleSituation(kills, deaths, assists, win)
  }

  // If it's a tie, return tie situation
  if (isTie) {
    const kda = calcKDA(kills || 0, deaths || 0, assists || 0)
    return { situation: "均势", color: "#607d8b", description: `对线均势 KDA:${kda}` }
  }

  // If no lane outcome data, return unknown
  if (laneWin === null || outcome === null) {
    return { situation: "未知", color: "#999", description: "无对线数据" }
  }

  const kda = calcKDA(kills || 0, deaths || 0, assists || 0)
  const kdaNum = parseFloat(kda)

  // Determine situation based on lane outcome and game outcome
  let situation, color, description

  if (laneWin && win) {
    // 对线赢 + 比赛赢
    if (isStomp || kdaNum >= 4 || (kills > deaths * 2)) {
      situation = "碾压"
      color = "#e91e63"  // 粉色-碾压
      description = isStomp ? `对线碾压！ KDA:${kda}` : `对线优势明显，击杀多项 KDA:${kda}`
    } else if (kdaNum >= 2) {
      situation = "优势"
      color = "#4caf50"  // 绿色-优势
      description = `对线占优，击杀多项 KDA:${kda}`
    } else {
      situation = "小优"
      color = "#8bc34a"  // 浅绿-小优
      description = `对线略占优势 KDA:${kda}`
    }
  } else if (!laneWin && !win) {
    // 对线输 + 比赛输
    if (isStomp || kdaNum < 0.5 || deaths > kills * 2) {
      situation = "被碾压"
      color = "#f44336"  // 红色-被碾压
      description = isStomp ? `对线被碾压！ KDA:${kda}` : `对线被压制，死亡过多 KDA:${kda}`
    } else if (kdaNum < 1) {
      situation = "劣势"
      color = "#ff9800"  // 橙色-劣势
      description = `对线处于下风 KDA:${kda}`
    } else {
      situation = "小劣"
      color = "#ffeb3b"  // 黄色-小劣
      description = `对线稍处下风 KDA:${kda}`
    }
  } else if (laneWin && !win) {
    // 对线赢 + 比赛输 - 队友崩了
    situation = "无奈"
    color = "#9c27b0"  // 紫色-无奈
    description = `对线优势但团队劣势 KDA:${kda}`
  } else {
    // 对线输 + 比赛赢 - 躺赢
    situation = "躺赢"
    color = "#00bcd4"  // 青色-躺赢
    description = `对线劣势但团队优势 KDA:${kda}`
  }

  return { situation, color, description }
}

// Analyze jungle situation based on KDA and match outcome
function analyzeJungleSituation(kills, deaths, assists, win) {
  const kda = calcKDA(kills || 0, deaths || 0, assists || 0)
  const kdaNum = parseFloat(kda)

  let situation, color, description

  if (win) {
    // 比赛赢
    if (kdaNum >= 5) {
      situation = "完美"
      color = "#e91e63"  // 粉色-完美
      description = `节奏完美，击杀多多 KDA:${kda}`
    } else if (kdaNum >= 3) {
      situation = "carry"
      color = "#4caf50"  // 绿色-carry
      description = `团队核心，带动全场 KDA:${kda}`
    } else if (kdaNum >= 1.5) {
      situation = "正常"
      color = "#8bc34a"  // 浅绿-正常
      description = `表现正常 KDA:${kda}`
    } else {
      situation = "混子"
      color = "#607d8b"  // 灰蓝-混子
      description = `躺赢选手 KDA:${kda}`
    }
  } else {
    // 比赛输
    if (kdaNum >= 3) {
      situation = "尽力"
      color = "#ff9800"  // 橙色-尽力
      description = `尽力局，队友太菜 KDA:${kda}`
    } else if (kdaNum >= 1) {
      situation = "一般"
      color = "#ffeb3b"  // 黄色-一般
      description = `中规中矩 KDA:${kda}`
    } else if (kdaNum >= 0.5) {
      situation = "逛街"
      color = "#ff5722"  // 深橙-逛街
      description = `基本没发挥作用 KDA:${kda}`
    } else {
      situation = "演员"
      color = "#f44336"  // 红色-演员
      description = `全场梦游 KDA:${kda}`
    }
  }

  return { situation, color, description }
}

// Analyze lane situations for multiple matches
export function analyzeLaneSituations(steamId, matches, heroes) {
  if (!matches || !matches.length) return { situations: [], summary: {} }

  // Create hero lookup map
  const heroMap = {}
  heroes.forEach(h => {
    heroMap[h.id] = h
  })

  const situations = []
  const situationCounts = {
    // 分路战况
    "碾压": 0, "优势": 0, "小优": 0, "均势": 0,
    "小劣": 0, "劣势": 0, "被碾压": 0, "无奈": 0, "躺赢": 0,
    // 打野战况
    "完美": 0, "carry": 0, "正常": 0, "混子": 0,
    "尽力": 0, "一般": 0, "逛街": 0, "演员": 0,
    "未知": 0
  }

  matches.forEach(match => {
    const player = match.players?.find(p => p.steamAccountId === parseInt(steamId))
    if (!player) return

    const hero = heroMap[player.heroId]
    const heroName = hero ? (hero.shortName || hero.displayName) : `Hero ${player.heroId}`

    // Get lane display name based on player's team (天辉/夜魇)
    const apiLane = player.lane
    const lane = getLaneDisplayName(apiLane, player.isRadiant)
    const outcomeField = getOutcomeField(lane, player.isRadiant)

    const matchData = {
      laneWin: null,
      win: player.isVictory,
      kills: player.kills,
      deaths: player.deaths,
      assists: player.assists,
      lane: lane,
      outcome: null,
      isJungle: apiLane === "JUNGLE"
    }

    if (outcomeField && match[outcomeField]) {
      const outcome = match[outcomeField]
      matchData.outcome = outcome

      if (outcome === "RADIANT_VICTORY" || outcome === "RADIANT_STOMP") {
        matchData.laneWin = player.isRadiant
        matchData.isStomp = outcome === "RADIANT_STOMP"
      } else if (outcome === "DIRE_VICTORY" || outcome === "DIRE_STOMP") {
        matchData.laneWin = !player.isRadiant
        matchData.isStomp = outcome === "DIRE_STOMP"
      } else if (outcome === "TIE") {
        matchData.laneWin = null
        matchData.isTie = true
      }
    }

    const situation = analyzeLaneSituation(matchData)

    situations.push({
      matchId: match.id,
      heroId: player.heroId,
      heroName: heroName,
      heroChineseName: HEROES_CN_BY_SHORTNAME[heroName] || hero?.displayName || heroName,
      ...matchData,
      situation: situation.situation,
      situationColor: situation.color,
      description: situation.description
    })

    situationCounts[situation.situation]++
  })

  // Calculate summary percentages
  const total = situations.length
  const summary = {}
  for (const [key, count] of Object.entries(situationCounts)) {
    if (count > 0) {
      summary[key] = {
        count: count,
        percentage: ((count / total) * 100).toFixed(1) + "%"
      }
    }
  }

  return { situations, summary }
}