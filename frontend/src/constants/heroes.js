export function getHeroChineseName(heroId) {
  const enName = HEROES_EN[heroId]?.toLowerCase()
  if (!enName) return `英雄${heroId}`

  // Convert display name format (anti-mage, crystal maiden) to shortName format (antimage, crystal_maiden)
  // Replace spaces with nothing for hyphenated names like "anti-mage" -> "antimage"
  // Replace spaces with underscores for multi-word names like "crystal maiden" -> "crystal_maiden"
  const shortNameForm = enName.replace(/-/g, '').replace(/ /g, '_')

  return HEROES_CN_BY_SHORTNAME[shortNameForm] || HEROES_CN_BY_SHORTNAME[enName.replace(/-/g, '')] || enName
}

export function getHeroEnglishName(heroId) {
  return HEROES_EN[heroId] || `Hero ${heroId}`
}

// Helper to normalize hero name from API
// API shortName format: antimage, crystal_maiden, drow_ranger, etc.
// API displayName format: Anti-Mage, Crystal Maiden, Drow Ranger, etc.
// Our HEROES_CN_BY_EN keys: anti-mage, crystal maiden, drow ranger (with spaces, hyphens for spaces)
function normalizeHeroName(name) {
  if (!name) return ''
  // If it's npc_dota_hero_xxx format, extract xxx
  if (name.startsWith('npc_dota_hero_')) {
    name = name.replace('npc_dota_hero_', '')
  }
  // Convert underscores to hyphens, then to spaces
  // antimage -> antimage
  // crystal_maiden -> crystal maiden -> crystal-maiden
  // drow_ranger -> drow ranger -> drow-ranger
  return name.replace(/_/g, ' ').replace(/-/g, ' ').toLowerCase().trim()
}

// Create map from API hero data to Chinese name
export function createHeroesMap(heroes) {
  if (!heroes) return {}
  const map = {}
  heroes.forEach(hero => {
    const id = hero.id
    const shortName = hero.shortName || ''

    // Direct lookup using the API's shortName format
    let cnName = HEROES_CN_BY_SHORTNAME[shortName]

    // Fallback: use displayName or shortName
    if (!cnName) {
      cnName = hero.displayName || shortName || `英雄${id}`
    }

    map[id] = {
      ...hero,
      chineseName: cnName
    }
  })
  return map
}

